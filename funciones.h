/** \brief Esta funcion toma dos numeros y los suma.
 *
 * \param Pide el primer numero.
 * \param Pide el segundo numero.
 * \return Devuelve el resultado de los dos numeros.
 *
 */
float suma(float, float);

/** \brief Esta funcion toma los dos numeros y los resta.
 *
 * \param Toma el primer numero.
 * \param Toma el segundo numero.
 * \return Devuelve el resultado de la resta de los dos numeros.
 *
 */
float resta(float, float);

/** \brief Esta funcion toma dos numeros y los multiplica.
 *
 * \param Toma el primer numero.
 * \param Toma el segundo numero.
 * \return Devuelve el resultado de la multiplicacion de los dos numeros.
 *
 */
float multiplicar(float, float);

/** \brief Esta funcion toma dos numeros y los divide.
 *
 * \param Toma el primer numero.
 * \param Toma el segundo numero.
 * \return Devuelve el resultado de la division de los dos numeros.
 *
 */
float dividir(float, float);

/** \brief Esta funcion toma un numero y realiza el factorial.
 *
 * \param Toma el numero.
 * \return Devuelve el resultado del factorial.
 *
 */
float factorial(float);


